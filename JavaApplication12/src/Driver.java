
public class Driver {

    public Driver() {

    }

    public void PressAccelerator() {
        System.out.println("Speed increases");
    }

    public void PressBrake() {
        System.out.println("Speed decreases");
    }

    public void ChangeGearForward() {
        System.out.println("Parking to drive mode");
    }

    public void ChangeGearBackward() {
        System.out.println("Drive to parking mode");
    }

    public void StartEngine() {
        System.out.println("Car is ready to run");
    }

    public void StopEngine() {
        System.out.println("Car is not ready to run");
    }

}
